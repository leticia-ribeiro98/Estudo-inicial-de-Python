#Faça um programa que leia três valores inteiros (a, b e c) e imprime a soma dos quadrados desses valores.

# -*- coding: utf-8 -*-

a = int (input('Digite o valor de a: '))
b = int (input('Digite o valor de b: '))
c = int (input('Digite o valor de c: '))
cal = (a**2) + (b**2) + (c**2)
print ('Resultado: %d' % cal)
